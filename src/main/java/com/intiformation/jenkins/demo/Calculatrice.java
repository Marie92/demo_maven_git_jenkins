public class Calculatrice {

	/**
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public int add (int a, int b) {
		return a+b;
	}//end add
	
	/**
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public int mul (int a, int b) {
		return a*b;
	}//end mul
	
}//end class
